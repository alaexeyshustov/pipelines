# frozen_string_literal: true

module Evaluation
  class SyntheticDatasetJob < ApplicationJob
    queue_as :default

    SYSTEM_PROMPT = <<~PROMPT.freeze
      You are a data generator. Given an AI agent's description and task, generate realistic
      input examples that the agent would receive in production.
      Return ONLY a JSON array of input objects. Each element must be a JSON object matching
      the agent's expected input schema. Do not include any explanation or markdown.
    PROMPT

    DEFAULT_MODEL = ENV.fetch("EVALUATION_LLM_MODEL", "gpt-5.4")

    def perform(draft_token:, agent_name:, dataset_name:, count:, hints: "", model: nil)
      system_prompt_text = fetch_system_prompt(agent_name)
      few_shot           = fetch_few_shot_samples(agent_name)
      model              = model || DEFAULT_MODEL

      user_message = build_user_message(
        system_prompt_text: system_prompt_text,
        count:              count,
        hints:              hints,
        few_shot:           few_shot
      )

      inputs = call_llm(user_message, model)

      dataset = create_dataset_with_records(dataset_name, agent_name, inputs)

      update_draft(draft_token, status: "complete", dataset_id: dataset.id)
    rescue StandardError => e
      logger.error("[SyntheticDatasetJob] Failed for draft #{draft_token}: #{e.message}")
      update_draft(draft_token, status: "error", error_message: e.message)
    end

    private

    def fetch_system_prompt(agent_name)
      Evaluation::Prompt
        .where(name: agent_name)
        .order(version: :desc, id: :desc)
        .first
        &.system_prompt
        .to_s
    end

    def fetch_few_shot_samples(agent_name)
      dataset = Evaluation::Dataset.where(name: agent_name).first
      return [] unless dataset

      dataset.dataset_records
             .joins("INNER JOIN action_runs ON evaluation_dataset_records.recordable_type = 'Orchestration::ActionRun' AND evaluation_dataset_records.recordable_id = action_runs.id")
             .limit(3)
             .pluck("action_runs.input")
             .compact
    end

    def build_user_message(system_prompt_text:, count:, hints:, few_shot:)
      parts = []
      parts << "## Agent System Prompt\n#{system_prompt_text}" if system_prompt_text.present?
      parts << "## Number of examples to generate\n#{count}"
      parts << "## Additional hints\n#{hints}" if hints.present?
      if few_shot.any?
        samples = few_shot.map { |s| JSON.pretty_generate(s) }.join("\n---\n")
        parts << "## Example real inputs (few-shot)\n#{samples}"
      end
      parts.join("\n\n")
    end

    def call_llm(user_message, model)
      response = RubyLLM.chat(model:)
                        .with_temperature(0.7)
                        .with_instructions(SYSTEM_PROMPT)
                        .ask(user_message)

      parsed = JSON.parse(response.content)
      raise ArgumentError, "Expected JSON array from LLM" unless parsed.is_a?(Array)

      parsed
    end

    def create_dataset_with_records(dataset_name, agent_name, inputs)
      ActiveRecord::Base.transaction do
        dataset = Evaluation::Dataset.create!(name: dataset_name, description: "Synthetic – generated by LLM")

        inputs.each do |input|
          synthetic = Evaluation::SyntheticRecord.create!(agent_name: agent_name, input: input)
          Evaluation::DatasetRecord.create!(dataset: dataset, recordable: synthetic)
        end

        dataset
      end
    end

    def update_draft(draft_token, status:, dataset_id: nil, error_message: nil)
      draft = Evaluation::WizardDraft.find_by(session_token: draft_token)
      return unless draft

      generation = { "status" => status }
      generation["dataset_id"]    = dataset_id    if dataset_id
      generation["error_message"] = error_message if error_message

      draft.merge_payload!("dataset_generation" => generation)
    end
  end
end
